/**
 * 現在時刻に基づいて、時間帯に合った挨拶メッセージをランダムに1つ返す。
 */
export function getGreeting(): string {
  const hour = new Date().getHours();

  let pool: string[];
  if (hour >= 5 && hour < 11) {
    pool = MORNING;
  } else if (hour >= 11 && hour < 18) {
    pool = AFTERNOON;
  } else if (hour >= 18 && hour < 24) {
    pool = EVENING;
  } else {
    pool = LATE_NIGHT;
  }

  return pool[Math.floor(Math.random() * pool.length)];
}
